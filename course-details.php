                            <h3>About this course</h3>
                            <p>College Writing 2.2x continues the introduction to academic and business writing for English Language Learners. In this part of the course, students focus on understanding the demands of different styles of writing, and continue to improve grammatical correctness, vocabulary development, and revision and editing skills. The five-week course includes material on vocabulary, tone, diction, and editing, all in the areas of academic and business writing. Students will have the opportunity to write on a number of different topics and ideas, always centered on areas of interest to them. The course materials will be offered via readings and videos. Assignments include short writing assignments, quizzes, journal entries, and longer pieces.</p>

